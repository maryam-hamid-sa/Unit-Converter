# Multi Unit Converter 

x = float(input("Enter any number: "))
 
print("your answer in meters      :", (x * 1000))
print("your answer in kilometers  :", (x / 1000))

print("your answer in centimeters :", (x * 2.54))
print("your answer in inches      :", (x / 2.54))

print("your answer in meters      :", (x * 0.3048))
print("your answer in feet        :", (x / 0.3048))

print("your answer in kilometers  :", (x * 1.60934))
print("your answer in miles       :", (x / 1.60934))
